

public class Maquina {
	int REGX, REGY, PC;

	public Maquina(int REGX, int REGY, int PC) {
		this.REGX = REGX;
		this.REGY = REGY;
		
	}

	public int getREGX() {
		return REGX;
	}

	public void setREGX(int rEGX) {
		REGX = rEGX;
	}

	public int getREGY() {
		return REGY;
	}

	public void setREGY(int rEGY) {
		REGY = rEGY;
	}

	public int getPC() {
		return PC;
	}

	public void setPC(int pC) {
		PC = pC;
	}
	
	
	
	
	
}
